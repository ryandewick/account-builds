#!/bin/bash

# Remove node_modules from Git's index
echo "Removing node_modules from Git's index..."
git rm -r --cached node_modules
git rm -r --cached client/node_modules
git rm -r --cached server/node_modules

# Stage the .gitignore file
echo "Staging .gitignore file..."
git add .gitignore

# Commit the changes
echo "Committing changes..."
git commit -m "Remove node_modules from Git tracking and update .gitignore"

echo "Done! node_modules are now properly ignored."
echo "Note: You may need to run 'git push' to update the remote repository."